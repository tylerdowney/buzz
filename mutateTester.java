public class mutateTester {
	private int var1;
	private int var2;
	public mutateTester(){
	var1 = 10;
	var2 = 20;
	}

	public void setVar1(int set){
		var1 = set;
	}

	public void setVar2(int set){
		var2 = set;
	}

	public int getVar1(){
		return var1;
	}

	public int getVar2(){
		return var2;
	}
}
